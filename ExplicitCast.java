class ExplicitCast
{
      public static void main(String ags[])

      {
               int a=100;
               System.out.println(a);
               byte b=(byte)600;
               
               // byte b=128;
                System.out.println(b);

                float f=78.95f;
                System.out.println(f);
                int c=(int)f;
                System.out.println(c);
     }
}